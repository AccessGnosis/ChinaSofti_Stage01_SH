package com.chinasofti.day06.tasks;

public class Di_05_GetSum01 {

	public static void main(String[] args) {
		System.out.println(getSum(3));
	}

	private static double getSum(int n) {
		double sum = 0.0;

		for (int i = 1; i <= n; ++i) {
			sum += (1.0 / i);
		}
		
		return sum;
	}
}

package com.chinasofti.day19.generic;

import java.util.Properties;

public class PropertiesDemo {

	public static void main(String[] args) {
		Properties p = new Properties();
		p.setProperty("周一", "礼拜一");
		p.setProperty("周二", "礼拜二");
		p.setProperty("周三", "礼拜三");
		p.setProperty("周四", "礼拜四");
		p.setProperty("周五", "礼拜五");
		p.setProperty("周六", "礼拜六");
		p.setProperty("周日", "礼拜日");
		System.out.println(p);
		System.out.println(p.getProperty("周一"));
		p.setProperty("周日", "礼拜天");
		System.out.println(p);
		
	}

}

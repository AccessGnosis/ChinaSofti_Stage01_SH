package com.chinasofti.day13.string;

public class StringDemo03 {

	public static void main(String[] args) {
		String str1 = "12abc3";
		String str2 = "12abc3";
		String str3 = "12" + "abc" + "3";
		String str4 = 12 + "abc3";
		String str5 = 12 + "abc" + 3;
		String str6 = "12";
		String str7 = "abc3" + str6;
		String str8 = 1 + '2' + "abc" + "3";
		String str9 = "1" + '2' + "abc" + 3;

		System.out.println(str1 + " hashCode=" +str1.hashCode()); // 1451991474
		System.out.println(str2 + " hashCode=" +str2.hashCode()); // 1451991474
		System.out.println(str3 + " hashCode=" +str3.hashCode()); // 1451991474
		System.out.println(str4 + " hashCode=" +str4.hashCode()); // 1451991474
		System.out.println(str5 + " hashCode=" +str5.hashCode()); // 1451991474
		System.out.println(str6 + " hashCode=" +str6.hashCode()); // 1569
		System.out.println(str7 + " hashCode=" +str7.hashCode()); // -1424434702
		System.out.println(str8 + " hashCode=" +str8.hashCode()); // 1565584557
		System.out.println(str9 + " hashCode=" +str9.hashCode()); // 1451991474
		
	}

}

class People {
	private String name;
	private String sex;
	private int age;
	private double high;

	public People() {
	}

	public People(String name, String sex, int age, double high) {
		this.name = name;
		this.sex = sex;
		this.age = age;
		this.high = high;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public double getHigh() {
		return high;
	}

	public void setHigh(double high) {
		this.high = high;
	}

	@Override
	public String toString() {
		return "People [name=" + name + ", sex=" + sex + ", age=" + age + ", high=" + high + "]";
	}

}